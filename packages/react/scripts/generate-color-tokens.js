import {
  blue,
  broadbandGreen,
  cashbackLilac,
  energyBlue,
  green,
  grey,
  insuranceOrange,
  mobileRose,
  orange,
  piggyPink,
  purple,
  red,
  warmWhite,
  yellow,
} from '../../tokens/js/color.js';
import ejs from 'ejs';
import fs from 'fs';
import path from 'path';

async function main() {
  const colorScales = {
    blue,
    broadbandGreen,
    cashbackLilac,
    energyBlue,
    green,
    grey,
    insuranceOrange,
    mobileRose,
    orange,
    piggyPink,
    purple,
    red,
    warmWhite,
    yellow,
  };

  const colors = Object.keys(colorScales)
    .map(scale => Object.keys(colorScales[scale]).map(step => `${scale}${step}`))
    .flat();

  const tokens = ejs.render(
    `// Do not edit directly, this file was generated by the gen:color-tokens script.
    export const colorTokens = [<% tokens.forEach(function(token) {%> "<%-token%>", <% }) %>] as const;`,
    { tokens: colors }
  );
  fs.writeFileSync(path.join('.', 'src', 'tokens', 'color.ts'), tokens, { encoding: 'utf8' });
}

main()
  .then(() => {
    console.log('Thanks, see ya 👋');
  })
  .catch(err => {
    console.error(err);
    return process.exit(1);
  });
