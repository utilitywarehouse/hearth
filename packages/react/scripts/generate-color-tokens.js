import { light, common } from '@utilitywarehouse/hearth-tokens/js/color.js';
import ejs from 'ejs';
import fs from 'fs';
import path from 'path';

async function main() {
  const commonColorTokens = Object.keys(common);
  const colorScaleTokens = Object.keys(light)
    .map(scale => Object.keys(light[scale]).map(step => `${scale}${step}`))
    .flat();
  const tokens = ejs.render(
    `// Do not edit directly, this file was generated by the gen:color-tokens script.
    export const colorTokens = [<% tokens.forEach(function(token) {%> "<%-token%>", <% }) %>] as const;`,
    { tokens: [...commonColorTokens, ...colorScaleTokens] }
  );
  fs.writeFileSync(path.join('.', 'src', 'tokens', 'color.ts'), tokens, { encoding: 'utf8' });
}

main()
  .then(() => {
    console.log('Thanks, see ya 👋');
  })
  .catch(err => {
    console.error(err);
    return process.exit(1);
  });
